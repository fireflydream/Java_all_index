package com.weirui.select;

public class DoubleFor01 {
    public static void main(String[] args) {
        int p = 2;
        int q = 5;
        for (int i = 0; i < p; i++) {
            for (int j = 0; j < q; j++) {
                System.out.println("i =" + i + "," + "j = " + j); // 执行语句的次数： p * q 次

            }

        }
    }
}
